            <br>
            <p>We'd been living apart and commuting to see each other for several months, but in the summer of 2013 Taylor took a job in Green Bay. We were excited to finally be together again, and we had to move out of our apartment in Middleton by the end of July. I proposed to Taylor on our last evening in Madison before moving the rest of our belongings to Northeast Wisconsin. On our way into Madison that evening, I took an early exit and parked us at a location on campus near the McClimon Soccer Complex. On our first date we had gone to a soccer game there, and it was on our walk back from that game that I nervously asked Taylor to be my girlfriend. So on this night, I brought her to the same path that we walked at the end of our first date. I gave her my iPad and I revealed this website to her. I then had her view a cheesy, romantic slideshow while we walked. The end of our walk coincided with the conclusion of the slideshow, and the final location overlooked Lake Mendota. It was here, under a beautiful sunset, that I proposed to Taylor.</p>
            <br>
            <p>I suppose she was right in the end: I did make her wait for forever. But it was our forever, and July 27, 2013 was the day I set it in motion.</p>
